/* What is Bubble Sort ?
Bubble Sort Algorithm is the simplest sorting algorithm that works by repeatedly swapping the adjacent elements
if they are in the wrong order. It is often used to introduce the concept of a sorting and is particularly suitable
for sorting small datasets.
*/

